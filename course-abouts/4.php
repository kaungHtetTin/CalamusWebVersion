        <h3>Description</h3>
        <h4> App Course Intro နှင့် Yonsei Textbookအကြောင်း</h4>

        <p>
            Level 3 courseတွင်မူ Koreaနိုင်ငံ၏ နာမည်အကြီးဆုံး Textbookများထဲမှ တစ်ခုဖြစ်သည့် Yonsei Textbookကို အသုံးပြ၍ ထည့်သွင်းသင်ကြားပေးထားပါသည်။ Yonsei Textbookတွင်မူ Level 3-1 နှင့် 3-2 ဟူ၍ နှစ်အုပ်ရှိပါသည်။ စာအုပ်တစ်အုပ်စီတွင် အခန်းကြီး ၅ခန်းစီ ပါဝင်ပြီး အခန်းကြီး တစ်ခန်း‌စီတွင် အခန်းသေးလေး ၄ခုစီ ပါဝင်ပါသည်။ Level 3 Courseတွင် Yonsei Textbookအတိုင်း Level 3-1 နှင့် Level 3-2ဟူ၍ section နှစ်ခုခွဲ၍ ထည့်သွင်း သင်ကြားပေးထားပါသည်။
        </p>

        <h4> Level 3-1 Course  </h4>

        <p>
           Level 3-1 Courseတွင် Yonsei book၏ အခန်းကြီးတစ်ခန်းစီကို Module 1,2,3,4,5ဟူ၍ ခွဲပေးထားပါသည်။ Module 1ခုစီတွင် Lesson 1-4ပါရှိ၍ Lesson တစ်ခုစီတိုင်းတွင် Grammar နှစ်ပုဒ်စီနှင့် 4Skills (Reading, Writing, Listening, Speaking) များကို မျှတစွာ ထည့်သွင်းသင်ကြားပေးထားပါသည်။ ထို့အပြင် ထပ်တိုးလေ့ကျင့်နိုင်ရန်အတွက် Additional Grammarနှင့် Grammar exercisesများပါ ထည့်သွင်းပေးထားပါသည်။ 
        </p>

        <h4>Daily Plan</h4>
        <p>
            Day planဖြင့် လေ့လာလိုသူများအတွက် Daily Planလည်း ထည့်သွင်းစီစဥ်ပေးထားပါသည်။ Daily Planတွင်မူ Lesson 1ခုစီနှင့် သက်ဆိုင်ရာ Grammar exercisesကို တစ်ရက်စာ အဖြစ် စီစဉ်ပေးထားပြီး ကျန်ရှိတဲ့ Additional Grammarကိုပါ တစ်ရက်စာအဖြစ် လေ့လာနိုင်အောင် စီစဉ်ပေးထားပါတယ်။ အဲ့အတွက်ကြောင့် Level 3-1 Courseကို စုစုပေါင်း 21Days လေ့လာရမည်ဖြစ်ပါသည်။
        </p>

        <h4> Our Support </h4>
