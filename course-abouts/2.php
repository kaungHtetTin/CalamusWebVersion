        <h3>Description</h3>
        <h4> Grammar, Speaking Skills </h4>
        <p>
            Level 1 Courseတွင် ဘာသာစကားတိုင်း၏ အရေးကြီးဆုံး Skillsများဖြစ်သည့် Grammar and Speaking သင်ခန်းစာများကို Part A, B, Cအဖြစ် သုံးပိုင်း ခွဲပေးထားပြီး တစ်ပိုင်းစီတိုင်းတွင် Grammar 10ခုနှင့် Speaking 5ခုကို ထည့်သွင်း သင်ကြားပေးထားပါသည်။ Lessonတစ်ခုစီတိုင်းကိုမူ Easy Korean၏ Teacherများ ကိုယ်တိုင်သင်ရိုးထုတ်နှုတ်၍ ထည့်သွင်း သင်ကြားပေးထားခြင်းဖြစ်ပါသည်။  
        </p>

        <h4>Reading Skill </h4>
        <p>
            Reading skillအတွက်မူ နာမည်ကြီး websiteတစ်ခုဖြစ်သည့် Talk to Me in Koreanမှ ထုတ်လုပ်ထားသည့် Easy Korean Reading Textbookကို ထည့်သွင်းပေးထားပြီး Readingတစ်ပုဒ်စီတိုင်းမှ သက်ဆိုင်ရာ စာပိုဒ်များနှင့်ဝေါဟာရများကို မြန်မာဘာသာပြန်ဆိုပေးထားပါသည်။
        </p>

        <h4>Daily Plan</h4>
        <p>
            Dayအလိုက် လေ့လာချင်သူများအတွက်လည်း 21Daysဆိုသည့် Daily planစီစဉ်‌ပေးထားပြီး Additional courseထဲမှ  Numberအပိုင်းနှင့်Vocabularyများကိုပါ ထပ်တိုးလေ့လာနိုင်မည်ဖြစ်ပါသည်။ 
        </p>

        <h4> Our Support </h4>
        <p>
            မသိနားမလည်သည့် သင်ခန်းစာများ ရှိပါက Easy Korean၏ Chat boxတွင် Teacher sectionမှတစ်ဆင့် Teacherများကို မေးမြန်းစုံစမ်းနိုင်ပြီး Telegram groupမှလည်း မေးမြန်းနိုင်ပါသည်။ Calamus Educationမှ လေ့လာသူအားလုံးအတွက် အကောင်းဆုံး ကြိုးစားလျက်ရှိပါသည်။
         
        </p>
