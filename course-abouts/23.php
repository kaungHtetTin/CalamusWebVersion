        <h3>Description</h3>
        <span class="_abc123"></span>
        <p>
            Easy English ရဲ့  Listening Course ကို Basic To Intermediate Level  အထိ Listening Skill ကို
            Training လုပ်နိုင်ရန် စနစ်တကျ ထည့်သွင်း ပြင်ဆင်ထားပါတယ်။
        </p>

        <p>
            ဤ Listening Course ကိုတက်ရောက်ရန် English စာ အခြေခံကောင်းများရှိဖို့လိုအပ်ပါတယ်။
            မိမိအနေဖြင့် English စာ အခြေခံအားသူဖြစ်ပါက Easy English ၏ Basic course ကို တက်ရောက်ဖို့ အကြံပြုပါသည်။
        </p>
        
        <p>
            သင်ခန်းစာ တစ်ခုစီတိုင်း Audio များ Video များနှင့် နားထောင်ပြီး တွဲဖက်လေ့ကျင့် နိုင်ရန် Exercise များကိုလည်း 
            စနစ်တကျ ထည့်သွင်းပေးထားပါတယ်။ 
        </p>

        <p>
            သင်ခန်းစာတစ်ခုကို တစ်ကြိမ်စီသာ နားထောင်ခြင်းမပြုဘဲ ထမင်းစားရေသောက် နားလည်သည်အထိ အကြိမ်ကြိမ်နားထောင်ပြီးမှ 
            နောက်ထပ်သင်ခန်းစာ တစ်ခုကို ဆက်လက်နား်ထောင်သွားရန် အကြုံပြုပါတယ်
        </p>

        <p>
            Listening Skill လုံးဝ အားနည်းသူများအတွက် Basic Category မှစပြီး သင်ခန်းစာ တစ်ခုကို အနည်းဆုံး ၁၀ ကြိမ်စီနားထောင်ဖို့ 
            အကြံပြုပါတယ် 
        </p>

        <p>
            Easy English ရဲ့ Listening Course ကို ပြီးဆုံးသွားပါက မိမိ၏ Listening Skill အနေအထား တစ်ခုထိ တိုးတက်သွားမှာ ဖြစ်ပါတယ်
        </p>

