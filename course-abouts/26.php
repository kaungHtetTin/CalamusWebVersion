        <h3>Description</h3>
        <span class="_abc123"></span>
        <p>
            Essential Speaking Course သည်  Elementary Level Course တစ်ခုဖြစ်ပါသည်။ English စကားပြောလေ့လာ လေ့ကျင့်နေသူများအတွက် 
            နေ့စဉ်ပြောဆိုရာတွင် ထိထိမိမိနှင့် ကျွမ်းကျွမ်းကျင်ကျင်ပြောဆိုနိုင်စေရန် များစွာအထောက်အကူပြုသော Course တစ်ခုဖြစ်ပါသည်။
        </p>

        <p>
           Essential Speaking Course တွင် Speaking like a native speaker, Speaking Pattern နှင့် Speaking topic များအကြောင်းကို လေ့လာရမှာဖြစ်ပါသည်။
           <strong>Speaking like a native speaker</strong> အပိုင်းတွင် <br>
           Native speaker များရဲ့ စကားပြောဆိုပုံ အနေအထားများ၊ အသံအနေအထားများနှင့် ဆင်တူရိုးမှားဖြစ်တတ်သော အမှားများ စသည့် အကြောင်းအရာများကို လေ့လာရမှာဖြစ်ပါသည်။ <br>
           <strong>Speaking Pattern </strong>အပိုင်းတွင် <br>
           နေ့စဉ်စကားပြောဆိုရာတွင် ထိထိမိမိဖြင့် ပြောဆိုနိုင်စေရန် မသိမဖြစ် သိရှိထားရမည့် pattern များကို လေ့လာရမှာဖြစ်ပါသည်။ <br>
           <strong>Speaking Topic</strong> အပိုင်းတွင် <br>
           သက်ဆိုင်ရာအကြောင်းအရာ (Topic) အလိုက် စကားပြောဆိုဟန်နှင့် အသုံးအနှုန်းများအကြောင်းကို လေ့လာရမှာဖြစ်ပါသည်။ <br>
        </p>


