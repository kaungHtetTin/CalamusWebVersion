        <h3>Description</h3>
        <h4> Grammar, Speaking Skills </h4>
        <p>
            Level 2Courseတွင် ဘာသာစကားတိုင်း၏ အရေးကြီးဆုံး Skillsများဖြစ်သည့် Grammar and Speaking သင်ခန်းစာများကို Part A, B, Cအဖြစ် သုံးပိုင်း ခွဲပေးထားပြီး တစ်ပိုင်းစီတိုင်းတွင် Grammar 10ခုနှင့် Speaking 5ခုကို ထည့်သွင်း သင်ကြားပေးထားပါသည်။ Lesson တစ်ခုစီတိုင်းကိုမူ Easy Korean၏ Teacherများ ကိုယ်တိုင်သင်ရိုးထုတ်နှုတ်၍ ထည့်သွင်း သင်ကြားပေးထားခြင်းဖြစ်ပါသည်။ ထို့အပြင် Level 2အတွက် Advance Grammar section အသစ်ကိုလည်း ထည့်သွင်းထားသည့်အတွက် Grammarများကို Level3 မတက်မီ ကြိုတင် လေ့လာထားနိုင်ပါသည်။
        </p>


