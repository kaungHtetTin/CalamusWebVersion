        <h3>Description</h3>
        <span class="_abc123"></span>
        <p>
        Easy English ၏ Elementary Course ကို Basic Course လေ့လာထားပြီးသူများ စတင်လေ့လာနိုင်ပါပြီ။ (၂၁) ရက်ကြာ လေ့လာရမည် ဖြစ်ပြီး ရက်အလိုက် လေ့လာရမည့် သင်ခန်းစာများကို ထည့်သွင်းဖော်ပြပေးထားပါသည်။
        </p>

        <h4>Elementary Grammar</h4>
        <p>
           Elementary Grammar ကို Note Book အပိုင်းမှ Basic Grammar  စာအုပ်နှင့် တွဲဖက်လေ့လာသင့်ပါသည်။
        </p>

        <h4>Reading</h4>
        <p>
           Reading အပိုင်းအတွင် Backward Translation Method ကို အသုံးပြုသင်ကြားပေးထားပြီး Reading Exercise များလည်းကိုလည်း ၎င်း  Translation Method များကို အသုံးပြုပြီး မိမိ၏ Reading Skill တိုးတက်အောင် လေ့ကျင့်သွားရမည်ဖြစ်ပါသည်။ အခြားသော ဝတ္တုတိုများ၊ ပုံပြင်များကိုလည်း ရှာဖွာကာ ၎င်း Translation Method  များအတိုင်း ဖတ်ရှုလေ့လာသင့်ပါသည်။
        </p>


        <h4>Speaking</h4>
        <p>
          Speaking အပိုင်း အနေဖြင့် နေ့စဉ်ဘဝအပြောအဆိုများတွင် အခြေခံ သိသင့်သည့် စကားပြောများကို ထည့်သွင်း ဖော်ပြပေးထားပြီး ထပ်တလဲလဲ လေ့ကျင့်ပြောဆိုသင့်ပါသည်။
        </p>

        <h4>Listening</h4>
        <p>
            အပိုင်းတွင်လည်း မိမိ၏ Listening Skill တိုးတက်စေရန် Audio (အသံ) များနှင့် နားထောင်ပြီး နားရည်ဝ သည်အထိ လေ့ကျင့်သွားရမည် ဖြစ်ပါသည်။
        </p>


